    public function update(Request $request, $id)
    {
        $validation=$request->validate(
            [
            'reply' => 'required|min:3'
        ]);
        
        $data=Contact::find($id);
            $details = [
                'Name' => $data->name,
                'MessageFrom'=> $data->message,
                'Message'=> $request->reply
            ];
            Mail::to($data->email)->send(new ReplyMail($details));
            $data->reply = 1;
            $data->user_id = Auth()->user()->id;
            $data->update();
            return redirect('admin/contact')->with('success', 'Your Reply Message Send');
